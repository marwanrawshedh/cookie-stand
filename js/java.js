function Cookies(avr, max, min) {
    this.avr = avr
    this.max = max
    this.min = min
    

    this.numco = function () {
        this.num = random(min, max) * this.avr
        
    }
}



function random(min, max) {
    return Math.floor(Math.random() * (max - min + 1) + min);
}



